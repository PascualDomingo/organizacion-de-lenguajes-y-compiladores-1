
package objetos;

public class Conjunto {
    private String identificador;
    private String miConjunto;

    public Conjunto(String identificador, String miConjunto) {
        this.identificador = identificador;
        this.miConjunto = miConjunto;
    }

    public String getIdentificador() {
        return identificador;
    }

    public void setIdentificador(String identificador) {
        this.identificador = identificador;
    }

    public String getMiConjunto() {
        return miConjunto;
    }

    public void setMiConjunto(String miConjunto) {
        this.miConjunto = miConjunto;
    }
    
    
    
}
